// 魹ｽ魹ｽ魹ｽ魹ｽﾋｵ魹ｽ魹ｽ     line        ﾄｿ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
// 魹ｽ魹ｽ魹ｽ魹ｽﾋｵ魹ｽ魹ｽ     *str        魹ｽ魹ｽﾏ｢
// 魹ｽ魹ｽ魹ｽﾘｲ魹ｽ魹ｽ魹ｽ     void
// ﾊｹ魹ｽ魹ｽﾊｾ魹ｽ魹ｽ     debug_output("Log message", file, line, str);
// 魹ｽ魹ｽﾗ｢魹ｽ魹ｽﾏ｢     魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾄｼ魹ｽ魹ｽﾚｲ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ 魹ｽﾃｻ魹ｽ魹ｽ魹ｽ魹ｽﾃｹ魹ｽﾗ｢ ﾒｲ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾞｸ魹ｽ
//-------------------------------------------------------------------------------------------------------------------
static void debug_output (char *type, char *file, int line, char *str)
{
    char *file_str;
    vuint16 i = 0, j = 0;
    vint32 len_origin = 0;
    vint16 show_len = 0;
    vint16 show_line_index = 0;
    len_origin = strlen(file);

    char output_buffer[256];
    char file_path_buffer[64];

    if(debug_output_info.type_index)
    {
        debug_output_info.output_screen_clear();
    }

    if(zf_debug_init_flag)
    {
        if(debug_output_info.type_index)
        {
            // 魹ｽ魹ｽﾒｪ魹ｽ魹ｽ魹ｽﾐｽ魹ｽ魹ｽﾄｼ魹ｽ魹ｽ魹ｽﾂｷ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
            // <魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾂｷ魹ｽ魹ｽ ﾖｻ魹ｽ魹ｽ魹ｽﾒｻ魹ｽ魹ｽﾄｿﾂｼ 魹ｽ魹ｽ魹ｽ魹ｽ src/main.c>
            // 魹ｽ魹ｽ魹ｽ line : xxxx
            debug_output_info.output_screen(0, show_line_index ++, type);

            file_str = file;
            len_origin = strlen(file);
            show_len = (debug_output_info.display_x_max / debug_output_info.font_x_size);

            while(*file_str++ != '¥0');

            // ﾖｻﾈ｡ﾒｻ魹ｽ魹ｽﾄｿﾂｼ 魹ｽ魹ｽ魹ｽ魹ｽﾄｼ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾌｷ魹ｽ魹ｽ魹ｽﾄｿﾂｼ 魹ｽ魹ｽ魹ｽ魹ｽ MDK 魹ｽﾄｹ魹ｽ魹ｽﾌｸ魹ｽﾄｿﾂｼ 魹ｽﾍｻ魹ｽﾖｱ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾇｰﾄｿﾂｼ
            for(j = 0; (j < 2) && (len_origin >= 0); len_origin --)             // 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ '/'
            {
                file_str --;
                if((*file_str == '/') || (*file_str == 0x5C))
                {
                    j ++;
                }
            }

            // 魹ｽﾄｼ魹ｽﾂｷ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ豬ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
            if(len_origin >= 0)
            {
                file_str ++;
                snprintf(output_buffer, sizeof(output_buffer), "file: %s", file_str);
            }
            else
            {
                if(0 == j)
                {
                    snprintf(output_buffer, sizeof(output_buffer), "file: mdk/%s", file_str);
                }
                else
                {
                    snprintf(output_buffer, sizeof(output_buffer), "file: %s", file_str);
                }
            }

            // 魹ｽ魹ｽﾄｻ魹ｽ魹ｽﾊｾﾂｷ魹ｽ魹ｽ
            for(i = 0; i < ((strlen(output_buffer) / show_len) + 1); i ++)
            {
                for(j = 0; j < show_len; j ++)
                {
                    if(strlen(output_buffer) < (j + i * show_len))
                    {
                        break;
                    }
                    file_path_buffer[j] = output_buffer[j + i * show_len];
                }

                file_path_buffer[j] = '¥0';                                     // ﾄｩﾎｲ魹ｽ魹ｽ魹ｽ魹ｽ¥0

                debug_output_info.output_screen(0, debug_output_info.font_y_size * show_line_index ++, file_path_buffer);
            }

            // 魹ｽ魹ｽﾄｻ魹ｽ魹ｽﾊｾ魹ｽﾐｺ魹ｽ
            snprintf(output_buffer, sizeof(output_buffer), "line: %d", line);
            debug_output_info.output_screen(0, debug_output_info.font_y_size * show_line_index ++, output_buffer);

            // 魹ｽ魹ｽﾄｻ魹ｽ魹ｽﾊｾ Log 魹ｽ魹ｽ魹ｽ魹ｽﾐｵﾄｻ魹ｽ
            if(NULL != str)
            {
                for(i = 0; i < ((strlen(str) / show_len) + 1); i ++)
                {
                    for(j = 0; j < show_len; j ++)
                    {
                        if(strlen(str) < (j + i * show_len))
                        {
                            break;
                        }
                        file_path_buffer[j] = str[j + i * show_len];
                    }

                    file_path_buffer[j] = '¥0';                                 // ﾄｩﾎｲ魹ｽ魹ｽ魹ｽ魹ｽ¥0

                    debug_output_info.output_screen(0, debug_output_info.font_y_size * show_line_index ++, file_path_buffer);
                }
            }
        }
        else
        {
            char output_buffer[256];
            memset(output_buffer, 0, 256);
            debug_output_info.output_uart(type);
            if(NULL != str)
            {
                snprintf(output_buffer, sizeof(output_buffer), "¥r¥nfile %s line %d: %s.¥r¥n", file, line, str);
            }
            else
            {
                snprintf(output_buffer, sizeof(output_buffer), "¥r¥nfile %s line %d.¥r¥n", file, line);
            }
            debug_output_info.output_uart(output_buffer);
        }
    }
}

