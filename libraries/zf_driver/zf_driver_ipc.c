//-------------------------------------------------------------------------------------------------------------------
void ipc_release_callback(void)
{
    ipc_released = 1;
}

//-------------------------------------------------------------------------------------------------------------------
// 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ     IPC魹ｽ魹ｽ魹ｽﾝｽ魹ｽ魹ｽﾕｻﾘｵ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
