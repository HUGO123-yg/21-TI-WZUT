// 魹ｽ魹ｽ魹ｽﾘｲ魹ｽ魹ｽ魹ｽ     void
// ﾊｹ魹ｽ魹ｽﾊｾ魹ｽ魹ｽ     
// 魹ｽ魹ｽﾗ｢魹ｽ魹ｽﾏ｢     魹ｽ魹ｽ魹ｽﾚｴﾋｺ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾝｺ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾃｻ魹ｽ魹ｽﾄｻﾘｵ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
//-------------------------------------------------------------------------------------------------------------------
void ipc_received_callback(uint32 *ipc_msg)
{
    communicate_data_t * data = (communicate_data_t *)ipc_msg;
    user_callback(data->data);
}


//-------------------------------------------------------------------------------------------------------------------
// 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ     IPC魹ｽ魹ｽ魹ｽﾝｷ魹ｽ魹ｽﾍｺ魹ｽ魹ｽ魹ｽ
// 魹ｽ魹ｽ魹ｽ魹ｽﾋｵ魹ｽ魹ｽ     send_data           魹ｽ魹ｽﾒｪ魹ｽ魹ｽ魹ｽﾍｸ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾒｻ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾄｵ魹ｽuint32魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
// 魹ｽ魹ｽ魹ｽﾘｲ魹ｽ魹ｽ魹ｽ     uint8               0魹ｽ魹ｽ魹ｽﾔｷ魹ｽ魹ｽﾉｹ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ      1魹ｽ魹ｽ魹ｽﾔｷ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾓｦ
// ﾊｹ魹ｽ魹ｽﾊｾ魹ｽ魹ｽ     ipc_send_data(0x00000001);
// 魹ｽ魹ｽﾗ｢魹ｽ魹ｽﾏ｢     
//-------------------------------------------------------------------------------------------------------------------
uint8 ipc_send_data(uint32 send_data)
{
    communicate_data_t pipedata;
    uint32 wait_time_us = 5000;
    uint8  return_data  = 1;
    pipedata.clientId   = 0x01;
    pipedata.data       = send_data;
    ipc_released        = 0;

    if (Cy_IPC_Pipe_SendMessage (ipc_port_save == 0 ? 1 : 0, &pipedata, ipc_release_callback) == CY_IPC_PIPE_SUCCESS)
    {
        while(wait_time_us)
        {
            if(ipc_released == 1)
            {
                return_data = 0;
                break;
            }
            else
            {
                system_delay_us(10);
                wait_time_us -= 10;
            }
        }
    }
    return return_data;
}



