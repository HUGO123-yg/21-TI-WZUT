#ifndef _euler_h_
#define _euler_h_

//===== 欧拉角数据结构 =====

#include "zf_common_typedef.h"

//===== Mahony 互补滤波增益 (可调) =====
#define MAHONY_KP           (2.0f)      // 比例增益 — 加速度计修正强度
#define MAHONY_KI           (0.0f)      // 积分增益 — 暂设为0，仅比例修正

//-------------------------------------------------------------------------------------------------------------------
// 结构体定义      euler_angle_struct
// 功能说明       欧拉角数据结构，存储飞行器/机器人姿态角度（单位：度）
// 成员说明       pitch   俯仰角   (绕X轴旋转，抬头上仰为正)
//               roll    横滚角   (绕Y轴旋转，右倾为正)
//               yaw     偏航角   (绕Z轴旋转，右转为正)
//-------------------------------------------------------------------------------------------------------------------
typedef struct
{
    float pitch;    // 俯仰角
    float roll;     // 横滚角
    float yaw;      // 偏航角
} euler_angle_struct;

//-------------------------------------------------------------------------------------------------------------------
// 函数名称       euler_init
// 功能说明       IMU 姿态初始化：陀螺仪零偏校准 + IMU660RA 初始化
//               校准流程：读取 100 次陀螺仪静止数据（间隔 1ms），计算平均值作为零偏补偿
// 输入参数       void
// 返回参数       void
// 调用示例       euler_init();           // 在 main() 中调用，位于时钟初始化之后
// 注意事项       调用此函数前请确保机器人处于静止状态，否则零偏校准不准
//               [TODO: 添加加速度计修正 (Mahony/Madgwick) 消除长期漂移]
//-------------------------------------------------------------------------------------------------------------------
void euler_init(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称       euler_update
// 功能说明       实时陀螺仪积分更新欧拉角（纯积分，无加速度计修正）
//               调用 imu660ra_get_gyro() 读取当前角速度，减去零偏后积分得到角度
//               [TODO: 添加加速度计修正 (Mahony/Madgwick) 消除长期漂移]
// 输入参数       dt          积分时间间隔（单位：秒），典型值 0.001f（1kHz 调用时）
// 返回参数       void
// 调用示例       euler_update(0.001f);    // 在 1kHz ISR 中调用
//-------------------------------------------------------------------------------------------------------------------
void euler_update(float dt);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称       euler_update_fused
// 功能说明       Mahony互补滤波姿态融合更新欧拉角（陀螺仪+加速度计）
//               使用加速度计测量重力方向修正陀螺仪积分漂移
//               ISR安全：无三角函数调用，使用快速反平方根归一化
// 输入参数       dt          积分时间间隔（单位：秒），典型值 0.001f（1kHz 调用时）
// 返回参数       void
// 调用示例       euler_update_fused(0.001f);    // 在 1kHz ISR 中调用，替代 euler_update()
//-------------------------------------------------------------------------------------------------------------------
void euler_update_fused(float dt);

extern euler_angle_struct euler_angle;

#endif
